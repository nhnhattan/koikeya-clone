$(document).ready(function () {
    let currentIndex = 0;
    if ($(window).width() <= 740) {
        $(".scroll-content").width = "3007px";
        $("#karamucho-top").attr("src", "./assets/Karamucho/Topbanner phone.png");
    } else {
        $(".scroll-content").width = "2867px";
        $("#karamucho-top").attr("src", "./assets/Karamucho/karamucho-top.png");
    }

    function updateButtons() {
        var scrollLeft = $("#scroll-content").scrollLeft();

        if (scrollLeft === 0) {
            $("#scroll-left").addClass("disable");
        } else {
            $("#scroll-left").removeClass("disable");
        }

        if ($(window).width() <= 740) {
            if (scrollLeft + $(".scroll-container").width() >= 4349) {
                $("#scroll-right").addClass("disable");
            } else {
                $("#scroll-right").removeClass("disable");
            }
        } else if ($(window).width() >= 740 && $(window).width() <= 1023) {
            if (scrollLeft + $(".scroll-container").width() >= 3399.305083984375) {
                $("#scroll-right").addClass("disable");
            } else {
                $("#scroll-right").removeClass("disable");
            }
        } else {
            if (scrollLeft + $(".scroll-container").width() >= 3300) {
                $("#scroll-right").addClass("disable");
            } else {
                $("#scroll-right").removeClass("disable");
            }
        }
    }

    $("#scroll-left").click(function () {
        if ($(window).width() <= 740) {
            $("#scroll-content").animate(
                {
                    scrollLeft: "-=355",
                },
                300,
                updateButtons
            );
        } else if ($(window).width() >= 740 && $(window).width() <= 1023) {
            $("#scroll-content").animate(
                {
                    scrollLeft: "-=275",
                },
                300,
                updateButtons
            );
        } else {
            $("#scroll-content").animate(
                {
                    scrollLeft: "-=280",
                },
                300,
                updateButtons
            );
        }
    });
    $("#scroll-right").click(function () {
        if ($(window).width() <= 740) {
            $("#scroll-content").animate(
                {
                    scrollLeft: "+=355",
                },
                300,
                updateButtons
            );
        } else if ($(window).width() >= 740 && $(window).width() <= 1023) {
            $("#scroll-content").animate(
                {
                    scrollLeft: "+=275",
                },
                300,
                updateButtons
            );
        } else {
            $("#scroll-content").animate(
                {
                    scrollLeft: "+=280",
                },
                300,
                updateButtons
            );
        }
    });
});

